#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>

void quickSort(int *data, int start, int end) {

	if (start >= end) { // 원소가 1개인 경우
		return;
	}

	int key = start; // 키는 첫 번째 원소
	int i = start + 1;
	int j = end;
	int temp;

	while (i <= j) { //엇갈릴 때까지 반복
		while (data[i] <= data[key] && i <= end) { // 키 값보다 큰 값을 만날 때 까지 이동
			i++;
		}
		while (data[j] <= data[key] && j > start) { // 키 값보다 작은 값을 만날 때 까지 반복
			j--;
		}
		if (i > j) { //현재 엇갈린 상태면 키 값과 교체
			temp = data[j];
			data[j] = data[key];
			data[key] = temp;
		}
		else {
			temp = data[j];
			data[j] = data[i];
			data[i] = temp;
		}
	}
	quickSort(data, start, j - 1);
	quickSort(data, j+1, end);

}


int main(void) {

	int N;
	int *data = NULL;
	
	scanf("%d", &N);
	data = (int *)malloc(sizeof(int) * N);

	for (int i = 0; i < N; i++) {
		scanf("%d", &data[i]);
	}

	quickSort(data, 0, N - 1);
	for (int i = 0; i < N; i++) {
		printf("%d ", data[i]);
	}

	free(data);
	return 0;
}