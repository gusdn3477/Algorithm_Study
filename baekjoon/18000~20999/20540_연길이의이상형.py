s = input()

for i in s:
    if i == 'E':
        print('I', end='')

    if i == 'I':
        print('E', end='')

    if i == 'S':
        print('N', end='')

    if i == 'N':
        print('S', end='')

    if i == 'T':
        print('F', end='')

    if i == 'F':
        print('T', end='')

    if i == 'J':
        print('P', end='')

    if i == 'P':
        print('J', end='')